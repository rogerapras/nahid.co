<p>ইন্টারফেস কে অবশ্যই <code>interface</code> কিওয়ার্ড দিয়ে লিখতে হবে। এটিও একধরনের ক্লাস কিন্তু একে সরাসরি ব্যবহার করা যাবেনা অবশ্য একে ব্যবহারের পূর্বে implement করে নিতে হবে। আর ইন্টারফেসের মেথডগুলোতে কোনো কোড বা ডিটেইল থাকবেনা। শুধু ডিকলারেশন থাকবে। যাইহোক যেহেতু ইন্টারফেস সরাসরি ব্যবহার করা যাবে না, তাই একে ইমপ্লিমেন্ট করে নিতে হবে। চলুন দেখি কিভাবে একে ইমপ্লিমেন্ট করবেনঃ</p>
<pre><code class="language-php hljs"><span class="hljs-class"><span class="hljs-keyword">class</span> <span class="hljs-title">Calculator</span> <span class="hljs-keyword">implements</span> <span class="hljs-title">Calculation</span>
</span>{
    <span class="hljs-keyword">public</span> <span class="hljs-function"><span class="hljs-keyword">function</span> <span class="hljs-title">sum</span><span class="hljs-params">(<span class="hljs-variable">$a</span>, <span class="hljs-variable">$b</span>)</span>

<span itemprop="articleBody">বেশ কিছুদিনপর আবারো কলম হাতে তুলে নিয়েছি, সরি কিবোর্ডে হাত দিয়েছি। আজকে চেষ্টা করবো এডভান্স প্রোগ্রামিং এর বহুল ব্যবহৃত ও সাধারন জনগনের কাছে ভিতিগ্রস্থ একটি বিষয় নিয়ে। আজকের বিষয় Polymorphism.</span> শব্দটা যতটা ভয়ানক পুরো কনসেপ্ট বা বিষয়টা ততটা ভয়ানক না। ট্রাস্ট মি এটি সত্যিই একটি সাধারন কনসেপ্ট। এমনকি আপনি প্রতিদিনই এ কনসেপ্ট নিয়ে কাজ করছেন নতুবা এটি বাস্তবিক জীবনে দেখছেন। বাস্তবিক জীবনে আমরা যা দেখি এসবই পলিমরফিক কন্সেপ্ট থেকে আসা। এমনকি আপনি নিজেও। কি? অবাক হলেন? চলুন বিস্তারিত জেনে নেই তাহলে আশা করি আর অবাক হবেন না।</p>
<h3>Polymorphism কি?</h3>
<p>আমি সবসময়ই বইয়ের টিপিক্যাল সঙ্গাকে অপছন্দ করি। তাই আমি যা বুঝেছি সেটা আমার মতো করেই বলার চেষ্টা করবো। Polymorphism এর বাংলায় অর্থ দ্বারায় বহুরূপতা। কি, সাথে সাথেই আপনার চোখে মেয়েদের চেয়ারা ভেসে উঠেছে? মেয়েরা একেক ছেলের সাথে একেক রকম ব্যবহার বা একেক সময়ে একেক রকমের আচরন করে। আর হ্যা পলিমরফিজম কিছুটা একই রকম। অর্থাৎ একই লোকের অবস্থাভেদে আচরনের ভিন্নতার নামই পলিমরফিজম। তাহলে প্রশ্ন হলো প্রোগ্রামিং’এ পলিমরফিজম কি? প্রোগ্রামিং’এ পলিমরফিজম হলো প্যারেন্ট ক্লাসের মেথডসমূহের একেক চাইল্ড ক্লাসে একেকরকম আচরন করা। অর্থাৎ প্যারেন্ট ক্লাসে মেথডের একটি ব্লুপ্রিন্ট থাকবে আর চাইল্ড ক্লাস আর চাইল্ড ক্লাস সেই মেথডগুলোর তাদের নিজের মতো করে ক্যারেক্টারিস্টিক সেট করবে। মনে হচ্ছে বুঝতে বুঝতে একটু কষ্ট হচ্ছে। সমস্যা নেই মাথা উপর থেকে যাবার আগেই আমরা একটা বাস্তব জীবন থেকে উদাহরন দেইঃ<br/>
ধরুন আপনার চাচার দুই ছেলে। একজন খুব ভদ্র আর একজন ইভটিজার। এখানে দুইজনই আপনার চাচার ছেলে, অর্থাৎ আপনার চাচা যদি ক্লাস হয় তবে ক্যারেক্টার হচ্ছে তার একটি মেথড। এখানে ছেলেরা হচ্ছে তার একেকটি চাইল্ড ক্লাস আর এই চাইল্ড ক্লাসগুলো আপনার চাচার ব্লুপ্রিন্ট থেকে নেয়া এবং তারা দুজেনই আপনার চাচার ছেলে কিন্তু তাদের বৈশিষ্ট্য বা চরিত্র ভিন্ন ভিন্ন।<br/>
জানি এখনো মাথা উপর দিয়েই যাচ্ছে অনেকের। ধৈর্য ধরে পরতে আগাতে থাকুন আশা করি আস্তে আস্তে মাথার নিচে নামবে।</p>
<p>পলিমরফিজমের প্রধান একটি বৈশিষ্ট্য হলো এর common interface. অর্থাৎ সকল চাইল্ড ক্লাসের কমন বৈশিষ্ট্যগুলো প্যারেন্ট ক্লাসে শুধু উল্লেখ করা থাকবে। চাইল্ড ক্লাসগুলো শুধু প্যানেন্ট ক্লাসের ব্লুপ্রিন্ট থেকে নিজেকে তৈরী করবে। Polymorphism কে দুটি উপায়ে বর্ননা করা যায় এবং এদের প্রত্যেকেরই নিজস্ব ব্যবহার আছে।</p>

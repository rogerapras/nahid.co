<h3>Interface</h3>
<p>Polymorphism এর একটি বহুল ব্যবহৃত অংশ হলো Interface. এটি OOP এর খুবই গুরুত্বপূর্ন একটি ফিচার। ইন্টারফেস বোঝার পূ্র্বে আমরা একটি উদাহরন দেখি, যারা ওয়েব ডেভলপমেন্টে কাজ করেন তারা এটি খুব ভালো বুঝবেন।
আমরা যখন কোনো একটি HTML থিম বানাই তার পূর্বে আমাদের একটি PSD ডিজাইন দেয়া হয়। এর মাধ্যমে আমরা বুঝতে পারি HTML দিয়ে আমাদের কি বানাতে হবে। অর্থাৎ এর রং কি হবে, এর মেনু কেমন হবে, এর বাটনগুলো কেমন হবে, এর ইনপুটগুলো দেখতে কেমন হবে এসবই বর্ননা করা থাকে PSD ডিজাইনে। কিন্তু আমরা চাইলেই কিন্তু PSD ডিজাইনকে সরাসরি ব্যবহার করতে পারি না অথবা ব্যবহার করা সম্ভবও না। এর মানে হলো আপনাকে অবশ্যই PSD থেকে HTML এ কনভার্ট করে একে ব্যবহার করতে হবে। আর একে বলা হয় ইমপ্লিমেন্টেশন। এখানে যদি আপনি PSD টিকে প্যারেন্ট ক্লাস ও HTML টিকে চাইল্ড ক্লাসের সাথে তুলনা করেন তাহলেই খেলা খতম। এখানে PSD টিই হলো টোটাল থিমের একটি ইন্টারফেস অর্থাৎ থিমটি কেমন হবে তা এখানেই বর্নানা করা হয়েছে আর প্রোগ্রামিং এর ইন্টারফেসও একই। আপনার একটি প্যারেন ক্লাস থাকবে যেখানে আপনি চাইল্ড কি কাজ করবে। কিন্তু কিভাবে কাজ করবে সেটি বলা থাকবে না। আর ইন্টারফেসে আপনি প্যারেন্ট ক্লাসকে সরাসরি ব্যবহার করতে পারবেন না সেজন্য অবশ্যই একে একবার ইমপ্লিমেন্ট করে নিতে হবে। তাহলে চলুন আমরা Interface এর একটি উদাহরন দেখিঃ</p>

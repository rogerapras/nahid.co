<p>আমি <code>Bird</code> ক্লাস থেকে <code>Parakeet</code> ও <code>Duck</code> দুটি ক্লাস তৈরী করেছি যার <code>canFly</code> ও <code>canSwim</code> হলো <code>Bird</code> এর পলিমরফিক এনটিটি আর <code>canSound</code> হলো <code>Bird</code> থেকে পাওয়া ডিফল্ট এনটিটি এবং <code>color</code> ও <code>isDomestic</code> কে <code>Parakeet</code> ও <code>Duck</code> ক্লাসের নতুন এককটি এনটিটি হিসেবে যুক্ত করলাম। ফলে এখন <code>Parakeet</code>এ চারটি মেথড কাজ করবে <code>canFly</code> <code>canSwim</code> <code>canSound</code> এবং <code>color</code> ও <code>Duck</code> এর চারটি মেথড <code>canFly</code> <code>canSwim</code> <code>canSound</code> ও <code>isDomestic</code>. এর ফলে <code>Bird</code> ক্লাস থেকে নতুন দুটি পাখি তৈরী করেছি টিয়া ও হাসকে। যেখানে টিয়া ও হাস উভয়েরই পাখির ডিফল্ট বৈশিষ্ট শব্দ করতে পারার ক্ষমতা রয়েছে। কিন্তু পাখি হলেও এরা এর প্যারেন্টকে ওভাররাইট করেছে ফলে এরা কেউ উড়তে পারে আবার কেউ পারেনা কেউ সাতার পারে আবার কেউ পারে না। আবার নিজেদের কিছু স্বতন্ত্র বৈশিষ্ট্যও রয়েছে।</p>
<p>Polymorphic ক্লাসের এটাই সুবিধা আপনি চাইলে এর মাধ্যেমে ক্লাসের একটি রেস্ট্রিকশন তৈরী করে দিতে পারেন যেটা তার চাইল্ড ক্লাস অবশ্যই ফলো করবে। আধুনিক ফ্রেমওয়ার্কগুলোতে এর প্রচুর ব্যবহার লক্ষ্য করবেন। মর্ডান প্রোগ্রামিং এ এটি অপরিহার্য একটি ফিচার।</p>
<p>এটি ব্যবহারের প্রধান কারন হচ্ছে ভিন্ন ভিন্ন পার্সপেক্টিভে একই ইন্টাফেস ব্যবহার করে আলাদা আউটপুট পাওয়া। এটি এমন একটা ক্ষেত্র যেখানে একাধিক ইমপ্লিমেন্টেশনের একটি নির্দিষ্ট ইন্টারফেস থাকবে অর্থাৎ সবাই একই কনভেনশন ফলো করবে।</p>
<p>যাইহোক আমি আমার লেখার একেবারে শেষে চলে এসেছি, আমি চেষ্টা করেছি সংক্ষিপ্ত আকারে পলিমরফিজমকে আপনাদের সামনে তুলে ধরতে। ভুলত্রুটি হলে অবশ্যই ধরিয়ে দিবেন, আর ভালো লাগলে জানাবেন।</p>
<p>ধন্যবাদ :)</p>

<p>ইমপ্লিমেন্ট করা কিছুটা ক্লাস এক্সটেন্ড করার মতো। শুধু এখানে অবশ্যই আপনাকে ইন্টারফেসে ডিকলার করা মেথডগুলোর ডিটেইল লিখতে হবে অর্থাৎ এর ফাংশনালি দিতে হবে। অবশ্য সাথে আপনি আপনার প্রয়োজনমতোও মেথড বা প্রোপার্টিজ দিয়ে আপনার কাজ চালাতে পারবেন। উপরে দেখুন আমরা <code>Calculator</code> ক্লাসটি <code>Calculation</code> ইন্টারফেস থেকে ইমপ্লিমেন্ট করেছি। আমরা <code>Calculator</code> ক্লাসে ইন্টারফেসের সকল মেথডের ফাংশনালিটিজ দিয়েছি সাথে একটা অতিরিক্ত মেথডও যুক্ত করেছি। এখানে অতিরিক্ত মেথড কিভাবে কি লিখবেন এর কোনো বাধ্যবাধকতা নেই কিন্তু ইন্টারেসে বর্ননা করা সকল মেথডকে ইমপ্লিমেন্টে হুবহু সে রকমের করেই বর্ননা করতে হবে।</p>
<h3>Abstract Classes</h3>
<p>Abstract class কিছুটা interface এর মতোই এর মূল পার্থক্য হলো এখানে আপনি মেথড এর নাম, ভিজিবিলিটি ও প্যারামিটার ডিকলার করা ছাড়াও চাইলে কিছু মেথডের বর্ননা করে দিতে পারবেন। Abstract class ও আপনি সরাসরি ব্যবহার করতে পারবেন না, একে অবশ্যই নূন্যতম একবার ইনহেরিট করে নিতে হবে। অর্থাৎ abstract class লিখাই হয় ইনহেরিট করে নেয়ার জন্য। চলুন একটি উদাহরন দেখি।</p>
<pre><code class="language-php hljs"><span class="hljs-keyword">abstract</span> <span class="hljs-class"><span class="hljs-keyword">class</span> <span class="hljs-title">Bird</span>
</span>{
    <span class="hljs-keyword">abstract</span> <span class="hljs-keyword">public</span> <span class="hljs-function"><span class="hljs-keyword">function</span> <span class="hljs-title">canFly</span><span class="hljs-params">()</span></span>;
    <span class="hljs-keyword">abstract</span> <span class="hljs-keyword">public</span> <span class="hljs-function"><span class="hljs-keyword">function</span> <span class="hljs-title">canSwim</span><span class="hljs-params">()</span></span>;

    <span class="hljs-keyword">public</span> <span class="hljs-function"><span class="hljs-keyword">function</span> <span class="hljs-title">canSound</span><span class="hljs-params">()</span>
    </span>{
        <span class="hljs-keyword">return</span> <span class="hljs-keyword">true</span>;
    }
}
</code></pre>
<p>খেয়াল করুন পিএইচপিতে abstract class কে <code>abstract</code> কিওয়ার্ড দিয়ে লিখতে হয়। আর যেসকল মেথডগুলোর ব্লুপ্রিন্ট তৈরী করবেন সেগুলোও <code>abstract</code> কিওয়ার্ড দিয়ে লিখতে হবে। এবং আপনি চাইলে এখানে মেথডের বর্ননাও লিখতে পারবেন। আমি এখানে একটি পাখির প্রতিচ্ছবি তৈরী করেছি যেখানে এটি থেকে যে পাখিই তৈরী করা হোকনা কেনো এর অবশ্যই ডানা ও পা থাকতে হবে আর ডিফল্ট এর আমি ভয়েস সিস্টেমও তৈরী করে দিয়েছি। তাহলে চলুন এই <code>Bird</code> ক্লাসটিকে দিয়ে এখন একটি Parakeet(টিয়া) ক্লাস তৈরী করবো যার অবশ্যই ডানা ও পা থাকতে হবেঃ</p>
<pre><code class="language-php hljs"><span class="hljs-class"><span class="hljs-keyword">class</span> <span class="hljs-title">Parakeet</span> <span class="hljs-keyword">extends</span> <span class="hljs-title">Bird</span>
</span>{
    <span class="hljs-keyword">public</span> <span class="hljs-function"><span class="hljs-keyword">function</span> <span class="hljs-title">canFly</span><span class="hljs-params">()</span>
